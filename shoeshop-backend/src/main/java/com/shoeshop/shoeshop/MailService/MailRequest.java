package com.shoeshop.shoeshop.MailService;

public class MailRequest {

    private String to;
    private String from;
    private String topic;
    private String text;

    public String getTo() {
        return to;
    }

    public String getFrom() {
        return from;
    }

    public String getTopic() {
        return topic;
    }

    public String getText() {
        return text;
    }
}
